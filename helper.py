from random import choice

def randblue():
  colors = ["darkblue", "blue", "lightskyblue", "lightsteelblue", "aliceblue"] 
  
  return choice(colors)

def randgreen():
  colors = ["green", "darkgreen", "forestgreen", "olivedrab"] 
  
  return choice(colors)

def randwhat():
  colors = ["green", "GreenYellow", "chartreuse", "LawnGreen", "SpringGreen"] 
  
  return choice(colors)

def randgray():
  colors = ["WhiteSmoke", "LightGray", "gray", "DarkGray", "Gainsboro"] 
  
  return choice(colors)

def randdirect():
  direct = [1, 2]

  return choice(direct)

def randname():
  name = ["haha", "too slow!", "oopsie", "boo!"]

  return choice(name)